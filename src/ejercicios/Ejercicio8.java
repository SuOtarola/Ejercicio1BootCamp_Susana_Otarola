package ejercicios;

public class Ejercicio8 {
    public static void main(String[] args) {
        /*8. Conversión de temperatura
        •	Descripción: Dado que la temperatura es c = 25 grados Celsius, conviértela a Fahrenheit usando la fórmula:
        •	Variables: c = 25.

        */
        double c = 25;

        // Convertir la temperatura a Fahrenheit
        double fahrenheit = (9.0 / 5.0) * c + 32;

        System.out.println(c + " grados Celsius es igual a " + fahrenheit + " grados Fahrenheit.");


    }
}

