package ejercicios;

public class Ejercicio6 {
    public static void main(String[] args) {
        /*6. Promedio de tres números
        •	Descripción: Dados los números a = 4, b = 9 y c = 13, calcula el promedio de los tres.
        •    Variables: a = 4, b = 9, c = 13.

        */
        int a = 4;
        int b = 9;
        int c = 13;

        // Calcular el promedio
        double promedio = (a + b + c) / 3.0;

        // Imprimir el resultado
        System.out.println("El promedio de los números " + a + ", " + b + " y " + c + " es " + promedio + ".");

    }
}

